package example;

public class Example03 {

	public static void main(String[] args) {
		int num = 0;
		int res = 0;
		
		while(num <=10){
			res += num++;
		}
		System.out.println(res);
		
		int sum= 0;
		for(int i=1; i<=10; i++) {
			sum += i;
		}
	
	System.out.println(sum);
	
	}

}
