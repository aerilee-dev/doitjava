package chapter03;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int intRes;
		double dRes;
		intRes = 3 + 2;
		System.out.println(intRes);
		intRes = 3 * 2;
		System.out.println(intRes);
		intRes = 3 / 2;
		System.out.println(intRes);
		dRes = 3.0 /2.0;
		System.out.println(dRes);
		intRes = 3 % 2;
		System.out.println(intRes);
		dRes = (double)3/2;
		System.out.println(dRes);
		
		
		
		
	}

}
